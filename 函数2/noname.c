#include<stdio.h>
int main()
{
	printf("%+d\n", 12);//总是显示正号...+12(使结果一直带有正负号）
	printf("%+d\n", -12);//...-12
	printf("%-d\n", -12);//...-12
	printf("%-d\n", +12);//...12
	printf("%.2f\n", 12.23);//限制小数点位数，".2"即小数点后两位
	printf("%.2f\n", 12.265);//若实际位数大于限制位数会进行四舍五入
	printf("%6.1f\n", 12.1515);//即限制总长度又限制小数点位数（6位，1小数点）
	printf("%*.*f\n",6,1,12.1515);//也可以像这样用*代替
	printf("%.2s\n", "zxcv");//只输出前两个字符
	return 0;
}