#define _CRT_SECURE_NO_WARNINGS 1
#include<stdio.h>
int main()
{
	char a = 20;
	//00010100
	//00000000000000000000000000010100提升
	//
	char b = 130;
	//10000010
	//11111111111111111111111110000010提升

	char c = a + b;
	//00000000000000000000000000010100
	//11111111111111111111111110000010
	//11111111111111111111111110010110
	//10010110--截断（只取char类型的8位）
	// 
	// 打印的时候还要提升一次
	//11111111111111111111111110010110
	//10000000000000000000000001101001--取反
	//10000000000000000000000001101010
	//
	printf("%d\n", c);//结果：-106

	//因为char类型取值范围是-128~127
	//取值范围是一个轮回，即127+1=-128
	//所以130对应为-126
	//+20=-106
	//
	return 0;
}