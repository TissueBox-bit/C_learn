#define _CRT_SECURE_NO_WARNINGS 1
#include<stdio.h>
int main()
{
	//按位与   &
	int a = 6;
	//00000000000000000000000000000110 -> 6的补码
	int b = -7;
	//10000000000000000000000000000111 -> -7的原码
	//11111111111111111111111111111000 -> 反码
	//11111111111111111111111111111001 -> 补码
	int c = a & b;//a和b的补码的二进制位进行运算
	//对应的二进制位，有0则为0，两个同时为1才为1
	//
	//00000000000000000000000000000110 -> 6的补码
	//11111111111111111111111111111001 -> 补码
	//00000000000000000000000000000000 -> 故结果是0
	printf("%d\n", c);

	//按位或   |
	int x = 6;
	//00000000000000000000000000000110 -> 6的补码
	int y = -7;
	//10000000000000000000000000000111 -> -7的原码
	//11111111111111111111111111111000 -> 反码
	//11111111111111111111111111111001 -> 补码
	int z = x | y;//a和b的补码的二进制位进行运算
	//对应的二进制位，有1则为1，两个同时为0才为0
	//
	//00000000000000000000000000000110 -> 6的补码
	//11111111111111111111111111111001 -> 补码
	//11111111111111111111111111111111 -> 故结果是-1
	printf("%d\n", z);



	//按位异或    ^
	int d = 6;
	//00000000000000000000000000000110 -> 6的补码
	int e = -7;
	//10000000000000000000000000000111 -> -7的原码
	//11111111111111111111111111111000 -> 反码
	//11111111111111111111111111111001 -> 补码
	int f = d ^ e;//a和b的补码的二进制位进行运算
	//对应的二进制位，相同为0，相异为1
	//
	//00000000000000000000000000000110 -> 6的补码
	//11111111111111111111111111111001 -> 补码
	//11111111111111111111111111111111 -> (补码)故结果是-1
	printf("%d\n", f);


	//按位取反    ~
	int g = 0;
	printf("%d\n", ~g);
	//按二进制位取反
	//00000000000000000000000000000000 -> 0的补码
	//11111111111111111111111111111111 -> (补码)故结果是-1
	return 0;
}